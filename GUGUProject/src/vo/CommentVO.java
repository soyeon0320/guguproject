package vo;

/**
 * 댓글 VO
 * @author ksy
 */
public class CommentVO {
	private String code; //코드
	private String content; //내용
	private String regDate; //등록일
	private String regName; //등록자
	private int parentLevel = 0; //부모레벨
	private int childLevel = 0; //자식레벨
	private String funBoardCode; //자유게시판 코드
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public int getParentLevel() {
		return parentLevel;
	}
	public void setParentLevel(int parentLevel) {
		this.parentLevel = parentLevel;
	}
	public int getChildLevel() {
		return childLevel;
	}
	public void setChildLevel(int childLevel) {
		this.childLevel = childLevel;
	}
	
	public String getRegName() {
		return regName;
	}
	public void setRegName(String regName) {
		this.regName = regName;
	}
	public String getFunBoardCode() {
		return funBoardCode;
	}
	public void setFunBoardCode(String funBoardCode) {
		this.funBoardCode = funBoardCode;
	}
	
	
}
