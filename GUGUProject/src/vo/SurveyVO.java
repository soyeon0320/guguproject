package vo;

import java.util.List;

/**
 * 설문조사 VO
 * @author pjy
 */

public class SurveyVO {
	private String surveyCode;// 설문코드
	private String surveyTitle;// 제목
	private String surveyStartdate; //시작일
	private String surveyEnddate; //종료일
	private String surveyIng; //시행여부
	private String surveyRegdate;// 등록일
	private String surveyExplain;// 설문설명
	
	private List<SurveyQuestionVO> questionList; // 설문에대한 질문목록
	
	public List<SurveyQuestionVO> getQuestionList() {
		return questionList;
	}
	public void setQuestionList(List<SurveyQuestionVO> questionList) {
		this.questionList = questionList;
	}
	public String getSurveyCode() {
		return surveyCode;
	}
	public void setSurveyCode(String surveyCode) {
		this.surveyCode = surveyCode;
	}
	public String getSurveyTitle() {
		return surveyTitle;
	}
	public void setSurveyTitle(String surveyTitle) {
		this.surveyTitle = surveyTitle;
	}
	public String getSurveyStartdate() {
		return surveyStartdate;
	}
	public void setSurveyStartdate(String surveyStartdate) {
		this.surveyStartdate = surveyStartdate;
	}
	public String getSurveyEnddate() {
		return surveyEnddate;
	}
	public void setSurveyEnddate(String surveyEnddate) {
		this.surveyEnddate = surveyEnddate;
	}
	public String getSurveyIng() {
		return surveyIng;
	}
	public void setSurveyIng(String surveyIng) {
		this.surveyIng = surveyIng;
	}
	public String getSurveyRegdate() {
		return surveyRegdate;
	}
	public void setSurveyRegdate(String surveyRegdate) {
		this.surveyRegdate = surveyRegdate;
	}
	public String getSurveyExplain() {
		return surveyExplain;
	}
	public void setSurveyExplain(String surveyExplain) {
		this.surveyExplain = surveyExplain;
	}
}
